def get_all_heroes() -> List[Dict[str, Any]]:
    """Get all heroes from all elements"""
    all_heroes = []
    all_heroes.extend(KIM_HEROES)
    all_heroes.extend(MOC_HEROES)
    all_heroes.extend(THUY_HEROES)
    all_heroes.extend(HOA_HEROES)
    all_heroes.extend(THO_HEROES)
    
    # Add image URLs to all heroes
    for hero in all_heroes:
        hero["icon_url"] = get_hero_image_url(hero["id"], hero["element"])
        hero["model_url"] = get_hero_model_url(hero["id"], hero["element"])
    
    return all_heroes


def get_heroes_by_element(element: str) -> List[Dict[str, Any]]:
    """Get all heroes of a specific element"""
    element_map = {
        "KIM": KIM_HEROES,
        "MOC": MOC_HEROES,
        "THUY": THUY_HEROES,
        "HOA": HOA_HEROES,
        "THO": THO_HEROES,
    }
    
    heroes = element_map.get(element.upper(), [])
    
    # Add image URLs
    for hero in heroes:
        hero["icon_url"] = get_hero_image_url(hero["id"], hero["element"])
        hero["model_url"] = get_hero_model_url(hero["id"], hero["element"])
    
    return heroes


def get_hero_by_id(hero_id: str) -> Dict[str, Any] | None:
    """Get a specific hero by ID"""
    all_heroes = get_all_heroes()
    for hero in all_heroes:
        if hero["id"] == hero_id:
            return hero
    return None


def get_hero_count() -> Dict[str, int]:
    """Get count of heroes by element"""
    return {
        "KIM": len(KIM_HEROES),
        "MOC": len(MOC_HEROES),
        "THUY": len(THUY_HEROES),
        "HOA": len(HOA_HEROES),
        "THO": len(THO_HEROES),
        "TOTAL": len(KIM_HEROES) + len(MOC_HEROES) + len(THUY_HEROES) + len(HOA_HEROES) + len(THO_HEROES),
    }
